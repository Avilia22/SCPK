            <p>
              
    <li>Protein</li>
        Protein adalah salah satu kandungan nutrisi penting pada buah alpukat. Alpukat menyediakan 18 asam amino esensial yang diperlukan bagi tubuh untuk membentuk protein lengkap. Berbeda dengan protein dalam daging yang sulit untuk dicerna, protein pada alpukat sangat mudah diserap oleh tubuh karena mengandung serat.

    <li>Lemak Bermanfaat</li>
        Tadi sudah dijelaskan kalau alpukat menyediakan jenis lemak sehat yang dibutuhkan tubuh. Seperti minyak zaitun, alpukat dapat meningkatkan kadar HDL (kolesterol "baik"). Kolesterol HDL dapat membantu melindungi terhadap kerusakan yang disebabkan oleh radikal bebas. Bahkan, kolesterol tipe ini juga dapat membantu mengatur kadar trigliserida serta mencegah diabetes.

    <li>Karotenoid</li>
        Alpukat merupakan sumber terbaik dari karotenoid dan phytonutrient. Alpukat, juga dikenal sebagai buah yang menawarkan beragam jenis karotenoid tidak hanya seperti beta-karoten, alfa-karoten dan lutein, tetapi juga varietas yang kurang dikenal dari jenis phytonutrient. contohnya neoxanthin, zeaxanthin, chrysanthemaxanthin, neochrome, beta-cryptoxanthin, dan violaxanthin.
        Mengonsumsi makanan yang kaya karotenoid akan memberikan tubuh banyak asupan vitamin A yang baik untuk kesehatan mata. Karotenoid juga meningkatkan fungsi sistem kekebalan tubuh dan kesehatan fungsi sistem reproduksi. Karotenoid dapat larut dalam lemak, sehingga dapat mengoptimalkan penyerapan nutrisi.

    <li>Lutein</li>
        Salah satu kandungan nutrisi yang paling penting dari alpukat adalah lutein, yaitu senyawa karotenoid, phytonutrient dan antioksidan alami. Satu buah alpukat mengandung 81 mcg lutein yang dapat membantu kesehatan mata. Mengonsumsi lutein dapat mengurangi risiko degenerasi makula mata karena usia.

    <li>Vitamin dan Mineral</li>
        Alpukat mengandung hampir 20 vitamin dan mineral yang berbeda, meskipun dalam jumlah kecil, menyediakan sekitar 10 persen kebutuhan vitamin dan mineral harian dalam setiap porsinya. Beberapa vitamin dan mineral yang paling banyak terkandung dalam buah alpukat adalah vitamin C, vitamin K, folat, kalium, zat besi, vitamin E, dan vitamin B6. 

    <li>Anti-inflamasi</li>
        Efek kombinasi dari nutrisi yang terkandung dalam alpukat menawarkan manfaat besar sebagai anti-peradangan. Kombinasi unik dari buah alpukat, seperti vitamin C dan E, karotenoid, selenium, seng, pitosterol dan omega-3 asam lemak membantu mencegah peradangan. Ini menunjukkan bahwa alpukat dapat membantu mencegah atau mengurangi risiko penyakit osteoarthritis dan rematik.

  <li>Natrium (Sodium) dan Kolestrol</li>
        Dalam sebuah alpukat mengandung 3 gram karbohidrat dalam setiap porsinya dan 1 gramnya adalah serat makanan (dietary fiber). Oleh karena itu, alpukat menjadi pilihan yang baik bagi orang-orang yang diet rendah karbohidrat.
        Di dalam alpukat terkandung folat, kalium, lemak monosaturated, dan serat yang sangat tinggi. Lemak monosaturated di dalam alpukat mengandung asam oleat, berfungsi untuk meningkatkan tingkat lemak di dalam tubuh dan membantu mengendalikan diabetes.
